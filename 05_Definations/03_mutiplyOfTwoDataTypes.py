# here we want to write a program where the defination supports polymorphism where if the parameter/placeholder have diffrent type of datatype then it also should multiply things

def sum_two_num(num1,num2):
    return num1*num2

print(sum_two_num('ujjwal',5))
print(sum_two_num('u',5))
print(sum_two_num(5,5))
print(sum_two_num(5,'ujjwal'))

